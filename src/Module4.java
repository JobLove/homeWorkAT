    public static int sumLastNums2(int x) {

        String str = String.valueOf(x);
        char[] chars = str.toCharArray();
        int[] arrayOfInt = new int[chars.length];
        for (int i = 0; i < chars.length; i++) {
            arrayOfInt[i] = Character.getNumericValue(chars[i]);
        }

        return arrayOfInt[arrayOfInt.length - 1] + arrayOfInt[arrayOfInt.length - 2];
    }

